#include <stdio.h>
int main(){
    /* escape sequence = characher combination consisting of a backslash \
                         follow by a letter or combination of digits.
                         They specify action within a line or sting of text.
                         \n = newline
                         \t = tab   
    */

   printf("Happy\nBirthday\nTo\nYou\n");
   printf("666\t999\n");
   printf("1\t2\t3\n4\t5\t6\n7\t8\t9\n \t0\t \n");
   return 0;
}